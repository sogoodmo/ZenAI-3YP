def create_plots(angles_score):
    """Create a plot of the score of the angles WRT to time. 

        The average of all of the scores should also be plotted and clearly distinguisable

    Args:
        angles_score (List[List[float]]): A list of all the angles score, each outer entry are the angles score for 1 frame, each inner entry maps to a specific joint. Labelled in ZenAI.py (joint_idx_map)
    """
    # Filter out empty sub-lists from X
    new_array = [elem for elem in angles_score if elem]

    # Extract the second element (score) from each tuple in new_array
    new_array = [[score for _, score in elem] for elem in new_array]
    
    # Taking every third frame and every second joint (So we don't have repeat of left shoulder vs rigth shoulder) since that data is pretty much the same 
    # And makes the graph more readable 
    angles_score = [new_array[i][::2] for i in range(0, len(new_array), 3)]
    # Convert the list of lists into a numpy array for easier processing
    angles_score = np.array(angles_score)
    
    # Compute the average score across all joints for each frame
    average_score = np.mean(angles_score, axis=1)
    
    # Create a figure and subplot
    fig, ax = plt.subplots(figsize=(12, 6))
    
    # Plot the score for each joint over time
    for joint_idx in range(angles_score.shape[1]):
        ax.plot(angles_score[:, joint_idx], label=f"{joint_idx_map[joint_idx*2].split(' ')[1]}")
    
    # Plot the average score over time
    ax.plot(average_score, label="Average Score", linewidth=4, linestyle="--", color="red")
    
    # Set the title and axis labels
    ax.set_title("Score of the Angles over Time")
    ax.set_xlabel("Time (frames) (Every 3 frames)")
    ax.set_ylabel("Score")
    
    # Add a legend to the plot
    ax.legend()

    # Save the plot
    fig.savefig("analytics.jpg")

